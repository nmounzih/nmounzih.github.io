$(document).ready(function(){
  $('#linkedIn').mouseenter(function(){
    $(this).animate({
      height: '+=5px'
    });
  });
  $('#linkedIn').mouseleave(function(){
    $(this).animate({
      height: '-=5px'
    });
  });
});

